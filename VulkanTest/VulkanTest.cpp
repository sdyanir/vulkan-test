inline void exitIfFailure(VkResult result, string msg) {
	if (result != VK_SUCCESS) {
		throw std::runtime_error(msg + ", error code: " + VkResultToString(result) + " (" + to_string(result) + ")");
	}
}

inline void tryCreate(VkResult result, string objectName) {
	exitIfFailure(result, "failed to create " + objectName + "!");
	cout << "successfully created " << objectName << endl;
}

template <class T>
inline T clamp(const T& v, const T& lo, const T& hi) {
	return max(lo, min(hi, v));
}

static std::vector<char> readFile(const std::string& filename) {
	std::ifstream file(filename, std::ios::ate | std::ios::binary);

	if (!file.is_open()) {
		throw std::runtime_error("failed to open file!");
	}

	size_t fileSize = (size_t)file.tellg();
	std::vector<char> buffer(fileSize);

	file.seekg(0);
	file.read(buffer.data(), fileSize);

	file.close();

	return buffer;
}

//vkCreateDebugReportCallbackEXT and DestroyDebugReportCallbackEXT are not automatically loaded, so have to be manually loaded.
VkResult CreateDebugReportCallbackEXT(VkInstance instance, const VkDebugReportCallbackCreateInfoEXT* pCreateInfo, const VkAllocationCallbacks* pAllocator, VkDebugReportCallbackEXT* pCallback) {
	auto func = (PFN_vkCreateDebugReportCallbackEXT)vkGetInstanceProcAddr(instance, "vkCreateDebugReportCallbackEXT");
	if (func != nullptr) {
		return func(instance, pCreateInfo, pAllocator, pCallback);
	}
	else {
		return VK_ERROR_EXTENSION_NOT_PRESENT;
	}
}

void DestroyDebugReportCallbackEXT(VkInstance instance, VkDebugReportCallbackEXT callback, const VkAllocationCallbacks* pAllocator) {
	auto func = (PFN_vkDestroyDebugReportCallbackEXT)vkGetInstanceProcAddr(instance, "vkDestroyDebugReportCallbackEXT");
	if (func != nullptr) {
		func(instance, callback, pAllocator);
	}
}


